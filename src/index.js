/* Operator */

// Assignment
let a = 1;
console.log(a);

let aa = 1;
aa += 2; // a = a + 2;
console.log(aa);

// Addition
let b = 2 + 2;
let c = "I am " + "a test";
console.log(b);
console.log(c);

// Multiplication
let d = 2 + 2 * 6 / 2;
console.log(d);

// Increment
let e = 1;
e++; // same than ++e;
console.log(e);

// Modulo
let f = 12 % 5;
console.log(f);

// Grouping
let g = (2 + 2) * 6 / 2;
console.log(g);
